HAL_STS_result_e HAL_StsTest(void){
	HAL_STS_result_e res = HAL_STS_RESULT_ERROR;
	uint16_t temp;
	uint8_t i=0;
	while (i<5){
		res = HAL_StsReadTemperature(&temp);
		if (res == HAL_STS_RESULT_ERROR) break;
		HAL_Delay(1000);
		i++;
	}
	return res;
}

