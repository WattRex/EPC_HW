HAL_SDS_result_e HAL_SdsTest(void){
	HAL_SDS_result_e res = HAL_SDS_RESULT_ERROR;
	uint16_t temp;
	uint8_t i=0;
	while (i<5){
		res = HAL_SdsReadTemperature(&temp);
		if (res == HAL_SDS_RESULT_ERROR) break;
		HAL_Delay(1000);
		i++;
	}
	return res;
}

